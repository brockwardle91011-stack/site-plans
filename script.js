// Ждем полной загрузки DOM, чтобы GSAP нашел все элементы
document.addEventListener("DOMContentLoaded", () => {
    
    // Регистрируем плагин ScrollTrigger (он нам понадобится для появления блоков при скролле)
    gsap.registerPlugin(ScrollTrigger);

    // Включаем анимацию Главного экрана (Hero)
    initHeroAnimation();
});

/**
 * Анимация Главного экрана
 * Реализуем плавное, "дорогое" появление элементов
 */
function initHeroAnimation() {
    // Создаем временную шкалу (Timeline), чтобы события шли друг за другом
    const tl = gsap.timeline({
        defaults: { 
            ease: "power4.out", 
            duration: 1.5 
        }
    });

    // 1. Проявляем монограмму "П" (плавно выплывает снизу) [cite: 4, 5]
    tl.to(".hero-monogram", {
        opacity: 1,
        y: 0,
        delay: 0.5
    });

    // 2. Анимируем заголовок (эффект вылета слов из-под невидимой маски) [cite: 10, 11]
    tl.to(".hero-title .word", {
        y: "0%",
        stagger: 0.1, // Задержка между словами для эффекта волны
        duration: 1.2
    }, "-=1"); // Начинаем чуть раньше, чем закончится анимация монограммы

    // 3. Проявляем подзаголовок (плавное появление с небольшим вылетом) [cite: 8, 9]
    tl.to(".hero-subtitle", {
        opacity: 0.6,
        y: 0,
        duration: 2
    }, "-=0.8");
}

